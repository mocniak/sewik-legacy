<h2 id="wyszukiwanie">Wyszukiwanie zdarzeń</h2>

<h3 id="baza_danych">Wybór bazy danych</h3>

<p>Ze względów wydajnościowych wstępna selekcja zdarzeń ma miejsce przez wybór odpowiedniej bazy danych.</p>

<ul>
<li>_rowery - wszystkie zdarzenia z udziałem rowerzystów w całej Polsce</li>
<li>_miasto - wszystkie zdarzenia z danego miasta</li>
</ul>

<h3 id="wyszukiwanie_podstawy">Podstawy</h3>

<p>Do danego wyszukania najczęściej nie trzeba wpisywać wszystkich danych, np. aby znaleźć wszystkie wypadki na terenie Warszawy wystarczy uzupełnić pole miejscowość. Nazwy jednostek administracyjnych (gmin, powiatów) zależnie od roku były różnie wpisywane w bazie, więc stosowanie najmniejszej koniecznej ilości informacji minimalizuje ryzyko błędu.</p>

<h3 id="wyszukiwanie_ulice">Nazwy ulic</h3>

<p>W zależności od regionu Polski Policja przyjmowała różne konwencje nazewnictwa ulic, placów, rond, itp. Również w obrębie pojedynczych miast zdarzają się niekonsekwencje we wpisywaniu miejsca zdarzenia - np. Plac Zawiszy w Warszawie występuje pod nazwami "Pl. Zawiszy", "Pl.Zawiszy" oraz "Plac Zawiszy". Przykłady nazw ulic jakie znajdują się w bazie dla danego rejonu można zobaczyć w wynikach wyszukiwania dla całego miasta.</p>

<h3 id="wyszukiwanie_skrzyżowania">Skrzyżowania</h3>

<p>Skrzyżowania można wyszukiwać wpisując w kolejne pola nazwy ulic dochodzących do szukanego miejsca. Pozostałe pola należy zostawić puste. Niestety nie ma skutecznej metody znalezienia wszystkich zdarzeń, jeżeli skrzyżowanie ma własną nazwę (np. "Rondo Jazdy Polskiej" czy ww. "Plac Zawiszy"). W takim przypadku trzeba wyszukiwać wszystkie możliwe kombinacje, nawet wpisując miejsce (plac, rondo) zdarzenia w polu "ulica", ponieważ część zdarzeń mające miejsce na placach nie są w ogóle oznaczone jako zdarzenia na skrzyżowaniach.</p>

<h3 id="wyszukiwanie_selekcja_użytkowników">Selekcja użytkowników</h3>

<p>Wyszukiwarka umożliwia selekcję zdarzeń na podstawie pojazdów uczestników (i pieszych) biorących udział w zdarzeniu.</p>

<p>Wybór sposobu selekcji "zdarzenia w których brały udział wszystkie wybrane grupy uczestników i tylko te" oznacza, że np. po wyborze pól "Rower" i "Samochód osobowy" bedą wyświetlane tylko zdarzenia w których rowerzyści mieli kolizje z samochodami.</p>

<p>Selekcja "zdarzenia w których brały udział wszystkie wybrane grupy uczestników, ale nie tylko te" oznacza, że wybierając pola "Rower" i "Samochód osobowy" wyświetlane będą zdarzenia w których uczestniczyli jednocześnie rowerzyści i samochody osobowe, ale dodatkowo dopuszcza się obecność innych uczestników ruchu (np. gdy samochód osobowy potrącił pieszego i rowerzystę).</p>

<p>Selekcja "zdarzenia w których brała udział przynajmniej jedna z wybranych grup uczestników" przydaje się do wyświetlania wszystkich zdarzeń dla danej grupy uczestników. Np. wybierając jednocześnie pola "Samochód ciężarowy do przewozu ładunków z przyczepą", "Samochód ciężarowy do przewozu ładunków bez przyczepy" i "Samochód ciężarowy do przewozu osób" wyświetlone zostaną wszystkie zdarzenia w których brały udział jakiekolwiek samochody ciężarowe.</p>

<p>Niektóre grupy użytkowników powtarzają się na liście wyboru - na przestrzeni lat zmieniał się słownik pojazdów biorących udział w zdarzeniu.</p>


